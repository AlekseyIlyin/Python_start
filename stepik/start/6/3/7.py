import math

a = float(input())
b = float(input())
c = float(input())
d = b**2 - 4 * a * c
if d < 0:
    print('–ù–µ—Ç –∫–æ—Ä–Ω–µ–π')
elif d == 0.0 or d == -0.0:
    print(-b / (2 * a))
else:
    x1 = (-b + math.sqrt(d)) / (2 * a)
    x2 = (-b - math.sqrt(d)) / (2 * a)
    print(min(x1, x2))
    print(max(x1, x2))