a = 61 #int(input())
b = 71 #int(input())
numberWithMaxSummDividers = a
maxSummDiv = 0
summDiv = 0
for num in range(a, b + 1):
    summDiv = 0
    for div in range(1, num + 1):
        if num % div == 0:
            summDiv += div
    if maxSummDiv <= summDiv:
        numberWithMaxSummDividers = num
        maxSummDiv = summDiv
print(numberWithMaxSummDividers, maxSummDiv)
